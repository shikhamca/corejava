package operator;

public class Chainassign {
	    public static void main(String args[]) {
	        int a, b, c;
	        a = b = c = 100; // set a, b, and c to 100
	        System.out.println("a = " + a);
	        System.out.println("b = " + b);
	        System.out.println("c = " + c);
	    }
	}


