package flowcontrol;

public class Ifelseifmultipleconditioncheck {
	public static void main(String[] args) {   

	      int number = 0;
		 
	      if (number > 0)//Expression
	     {
	         System.out.println("Number is positive.");//code
	      }
	      else if (number < 0) //Expression
	      {
	         System.out.println("Number is negative.");//code
	      }
	      else {
	         System.out.println("Number is 0.");//code
	      } 
	   }

}
